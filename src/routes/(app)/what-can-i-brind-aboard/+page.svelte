		reasonable range is 1 luggage with maximum dimensions of 80 x 50 x 35 cm and
		weighs no more than 20 kg. In addition to this, a small bag (purse, laptop
		bag, etc.). It is not allowed to bring items that may pose a safety hazard
		or disturb other travellers. For safety reasons, service personnel may deny
		anyone who does not comply with our luggage rules to bring their luggage on
		board.
	</p>
	<h3>For those travelling with stroller</h3>
	<p>
		A stroller may be taken onboard, one stroller per booking. The stroller is
		to be folded up and placed in special luggage shelves or at the location
		designated by the service personnel. For safety reasons, the stroller must
		not be placed in the vehicle's aisles. We cannot guarantee that your
		stroller can be placed in the section in which you are sitting. If you are
		unsure what applies on board, you can always contact the service staff who
		will be happy to assist you.
	</p>
	<h3>Bicycle</h3>
